//FOR SENTENCE
#include <iostream>

using namespace std;

int main () {
    /*  El bucle for funciona de la misma manera que un bucle while 
        con la diferencia de que se declara el contador, la expresion  y el aumento
        del contador en la misma estructura del for
    */
    
    for(int i = 1; i<=10; i++){ // Por convencion el contador es nombrado como i
        cout<<"Valor del contador: "<<i<<endl;
    }

    return 0;
}