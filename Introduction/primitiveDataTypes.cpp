// DATOS PRIMITIVOS

#include <iostream> 

using namespace std; 

int main() {

    int intNumber = 8; // Para almacenar numeros enteros
    float floatNumber = 8.3; // Para almacenar numero flotantes
    double bigFloatNumber = 6.74; // Para almacenar numeros flotantes con mas capacidad de digitos
    char character = 'A'; // Para almacenar un solo caracter, cualquiera del codigo ASCII

    cout<<"Este es un numero entero: "<<intNumber<<endl;
    cout<<"Este es un numero flotante: "<<floatNumber<<endl;
    cout<<"Este es un numero flotante con mas capacidad: "<<bigFloatNumber<<endl;
    cout<<"Este es un caracter: "<<character<<endl;

    return 0;
}